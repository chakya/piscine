#include	<unistd.h>

int	main(int argc, char *argv[])
{
	char	*s;
	int		i;

	if (argc == 1)
	{
		i = 0;
		s = argv[0];
		while (*s)
		{
			s++;
			i++;
		}
		write(1, argv[0], i);
		write(1, "\n", 1);
	}
	return (0);
}
