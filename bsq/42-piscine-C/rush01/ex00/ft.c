#include <unistd.h>

void	myft_putchar(char c)
{
	write(1, &c, 1);
}

void	myft_putstr(char *str)
{
	char *c;

	c = str;
	while (*c != '\0')
	{
		myft_putchar(*c);
		c++;
	}
}
